% plot delH vs. delV for the Earth-like experiments

%------------------------size of figure------------------------------------
figureHeight = 3.5; % unit inches
width2height= 1;
figure1 = figure('unit','inches','position',[0 0 figureHeight*width2height figureHeight]);

fontname = 'Helvetica';
set(0,'defaultaxesfontname',fontname);
fontsize1 = 12;
fontsize2 = 11;

% ======================== plot Earth-like case with different gamma ======

colorScheme = 'bo';
colorMarker = 'b';

refX  = 5:10:100;
refY  = refX;
plot(refX,refY,'--k','linewidth',2)
hold on

for expNum = 1:4
    plot(delH_earthLike(:,expNum),delV_earthLike(:,expNum),['--' colorScheme],...
    'linewidth',1,'markeredgecolor',colorMarker,'MarkerFaceColor',colorMarker,...
    'markersize',4);

end
plot(delH_earthLike(4,:),delV_earthLike(4,:),'-k','linewidth',2)
axis square
box on

%xlabel({'-$(f/\beta){\partial}_y \overline {\theta}$'},...
